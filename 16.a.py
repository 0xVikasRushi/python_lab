set1 = {1,2,3,4,5}
set2 = {6,5,4,3,2,2}
print(set1|set2) #UNION merge both sorted list


print(set1&set2) #intersection common element both sets


print(set1-set2)
#Returns a set containing elements only
# in the first set, but not in the second set.



print(set1^set2)
#Returns a set consisting
# of elements in both sets,
# excluding the common elements.